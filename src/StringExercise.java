import java.util.Scanner;
public class StringExercise {
    public static void main(String[] args){
        String sent = "We don't need no education \nWe don't need no thought control\n";
        System.out.println(sent);

        sent = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(sent);

        sent = "In windows, the main drive is usually C:\\\n";
        System.out.println(sent);

        sent = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(sent);
    }
}
